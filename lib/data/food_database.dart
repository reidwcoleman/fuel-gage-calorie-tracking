  static List<String> get categories =>
      foods.map((f) => f.category).toSet().toList()..sort();

  static List<FoodItem> search(String query) {
    final lowerQuery = query.toLowerCase();
    return foods
        .where((food) => food.name.toLowerCase().contains(lowerQuery))
        .toList();
  }

  static List<FoodItem> getByCategory(String category) {
    return foods.where((food) => food.category == category).toList();
  }
